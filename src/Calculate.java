public class Calculate {

    public double sum (double num, double num2 ){
        return num + num2;
    }
    public double sub (double num, double num2 ){
        return num - num2;
    }
    public double mul (double num, double num2 ){
        return num * num2;
    }
    public double div (double num, double num2 ){
        return num / num2;
    }
}
